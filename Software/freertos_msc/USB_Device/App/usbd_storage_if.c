/**
  * @brief  Initializes over USB FS IP
  * @param  lun:
  * @retval USBD_OK if all operations are OK else USBD_FAIL
  */
int8_t STORAGE_Init_FS(uint8_t lun)
{
  /* USER CODE BEGIN 2 */

	if(hspif1.Inited==1){
		return (USBD_OK);
	}
	else {
		if (SPIF_Init(&hspif1, &hspi1, GPIOB, GPIO_PIN_7) != true) {
		        return USBD_FAIL;
		    }


	}
  return (USBD_OK);
  /* USER CODE END 2 */
}

