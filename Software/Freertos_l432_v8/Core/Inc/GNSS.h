union u_Short
{
	uint8_t bytes[2];
	unsigned short uShort;
};

union i_Short
{
	uint8_t bytes[2];
	signed short iShort;
};

union u_Long
{
	uint8_t bytes[4];
	unsigned long uLong;
};

union i_Long
{
	uint8_t bytes[4];
	signed long iLong;
};

typedef struct
{
	UART_HandleTypeDef *huart;

	uint8_t uniqueID[4];
	uint8_t uartWorkingBuffer[101];
	uint8_t received_flag;
	unsigned short year;
	uint8_t yearBytes[2];
	uint8_t month;
	uint8_t day;
	uint8_t hour;
	uint8_t min;
	uint8_t sec;
	uint8_t fixType;//2==2d fix and 3==3dfix 0==no fix and 1==deadreckoning
	uint8_t numSV;//sat number used in nav solution
	uint8_t satCount;

	signed long lon;
	uint8_t lonBytes[4];
	signed long lat;
	uint8_t latBytes[4];
	float fLon;
	float fLat;

	signed long height;
	float fheight;


	signed long hMSL;
	uint8_t hMSLBytes[4];
	float fhMSL;

	unsigned long hAcc;
	float fhACC;
	unsigned long vAcc;
	float fvACC;

	signed long gSpeed;
	uint8_t gSpeedBytes[4];
	float fgSpeed;

	signed long headMot;

	float distance_parcouru;

}GNSS_StateHandle;


